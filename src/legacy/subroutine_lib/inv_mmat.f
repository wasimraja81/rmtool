! This subroutine calculates the inverse of any
! 4x4 matrix. 
! 
! It was designed to calculate the inverse of the 
! Mueller Matrix derived using code "mygpcal.f". 
! 
! This subroutine is specific to 4x4 matrices only.
! 
!          --wasim raja, rri, 14 Dec, 2009.
! 
chelp-

      subroutine inv_mmat(A,I)

      implicit none

      real*4 A(4,4), I(4,4)

      real*4 a11, a12, a13, a14
      real*4 a21, a22, a23, a24
      real*4 a31, a32, a33, a34
      real*4 a41, a42, a43, a44

      real*4 i11, i12, i13, i14
      real*4 i21, i22, i23, i24
      real*4 i31, i32, i33, i34
      real*4 i41, i42, i43, i44


      a11 = A(1,1)
      a12 = A(1,2)
      a13 = A(1,3)
      a14 = A(1,4)

      a21 = A(2,1)
      a22 = A(2,2)
      a23 = A(2,3)
      a24 = A(2,4)

      a31 = A(3,1)
      a32 = A(3,2)
      a33 = A(3,3)
      a34 = A(3,4)

      a41 = A(4,1)
      a42 = A(4,2)
      a43 = A(4,3)
      a44 = A(4,4)


      i11 = (-a24*a33*a42 + a23*a34*a42 + a24*a32*a43 - a22*a34*a43 - 
     -  a23*a32*a44 + a22*a33*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i12 = (a14*a33*a42 - a13*a34*a42 - a14*a32*a43 + a12*a34*a43 + 
     -  a13*a32*a44 -
     -    a12*a33*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i13 = (-a14*a23*a42 + a13*a24*a42 + a14*a22*a43 - a12*a24*a43 - 
     -  a13*a22*a44 + a12*a23*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i14 = (a14*a23*a32 - a13*a24*a32 - a14*a22*a33 + a12*a24*a33 + 
     -  a13*a22*a34 -
     -    a12*a23*a34)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i21 = (a24*a33*a41 - a23*a34*a41 - a24*a31*a43 + a21*a34*a43 + 
     -  a23*a31*a44 -
     -    a21*a33*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)


      i22 = (-a14*a33*a41 + a13*a34*a41 + a14*a31*a43 - a11*a34*a43 - 
     -  a13*a31*a44 + a11*a33*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i23 = (a14*a23*a41 - a13*a24*a41 - a14*a21*a43 + a11*a24*a43 + 
     -  a13*a21*a44 -
     -    a11*a23*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i24 = (-a14*a23*a31 + a13*a24*a31 + a14*a21*a33 - a11*a24*a33 - 
     -  a13*a21*a34 + a11*a23*a34)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i31 = (-a24*a32*a41 + a22*a34*a41 + a24*a31*a42 - a21*a34*a42 - 
     -  a22*a31*a44 + a21*a32*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i32 = (a14*a32*a41 - a12*a34*a41 - a14*a31*a42 + a11*a34*a42 + 
     -  a12*a31*a44 -
     -    a11*a32*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i33 = (-a14*a22*a41 + a12*a24*a41 + a14*a21*a42 - a11*a24*a42 - 
     -  a12*a21*a44 + a11*a22*a44)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i34 = (a14*a22*a31 - a12*a24*a31 - a14*a21*a32 + a11*a24*a32 + 
     -  a12*a21*a34 -
     -    a11*a22*a34)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i41 = (a23*a32*a41 - a22*a33*a41 - a23*a31*a42 + a21*a33*a42 + 
     -  a22*a31*a43 -
     -    a21*a32*a43)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i42 = (-a13*a32*a41 + a12*a33*a41 + a13*a31*a42 - a11*a33*a42 - 
     -  a12*a31*a43 + a11*a32*a43)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i43 = (a13*a22*a41 - a12*a23*a41 - a13*a21*a42 + a11*a23*a42 + 
     -  a12*a21*a43 -
     -    a11*a22*a43)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)

      i44 = (-a13*a22*a31 + a12*a23*a31 + a13*a21*a32 - a11*a23*a32 - 
     -  a12*a21*a33 + a11*a22*a33)/(a14*a23*a32*a41 - a13*a24*a32*a41 - 
     -   a14*a22*a33*a41 + a12*a24*a33*a41 + a13*a22*a34*a41 - 
     -   a12*a23*a34*a41 - a14*a23*a31*a42 + a13*a24*a31*a42 + 
     -   a14*a21*a33*a42 - a11*a24*a33*a42 - a13*a21*a34*a42 + 
     -   a11*a23*a34*a42 + a14*a22*a31*a43 - a12*a24*a31*a43 - 
     -   a14*a21*a32*a43 + a11*a24*a32*a43 + a12*a21*a34*a43 - 
     -   a11*a22*a34*a43 - a13*a22*a31*a44 + a12*a23*a31*a44 + 
     -   a13*a21*a32*a44 - a11*a23*a32*a44 - a12*a21*a33*a44 + 
     -   a11*a22*a33*a44)


      I(1,1) = i11
      I(1,2) = i12
      I(1,3) = i13
      I(1,4) = i14

      I(2,1) = i21
      I(2,2) = i22
      I(2,3) = i23
      I(2,4) = i24

      I(3,1) = i31
      I(3,2) = i32
      I(3,3) = i33
      I(3,4) = i34

      I(4,1) = i41
      I(4,2) = i42
      I(4,3) = i43
      I(4,4) = i44


      return
      end
